    private void imprimirDadosPlanos(List<Plano> listaDePlanos) throws Exception {
        DefaultTableModel model = (DefaultTableModel) jTablePlano.getModel();
        model.setNumRows(0);
        plaBll.ordenaListaPlanos(listaDePlanos);
        for (int pos = 0; pos < listaDePlanos.size(); pos++) {
            String[] linha = new String[4];
            Plano plano = listaDePlanos.get(pos);
            linha[0] = "" + plano.getId();
            linha[1] = plano.getDescricao().toUpperCase();
            linha[2] = "" + plano.getAcessoSimultaneo();
            linha[3] = String.format("%.2f", plano.getPreco());
            model.addRow(linha);
        }
        jTextFieldQuantRegistros.setText(listaDePlanos.size() + "");
    }

    public void limpaCampos() {
        jTextFieldCodigo.setText("");
        jTextFieldDescPlano.setText("");
        jTextFieldQtdeAcessoSimultaneos.setText("");
        jTextFieldValorDoPlano.setText("");
        jTextFieldQuantRegistros.setText("");
        jButtonExcluir.setEnabled(false);
        jButtonSalvar.setLabel("Salvar");
        jTextFieldDescPlano.requestFocus();
    }

    public void preencherCampos(int id) {
        try {
            if (id > 0) {
                plano = plaBll.getConsultaPorId(id);
                jTextFieldCodigo.setText(id + "");
                jTextFieldDescPlano.setText(plano.getDescricao());
                jTextFieldQtdeAcessoSimultaneos.setText(plano.getAcessoSimultaneo() + "");
                jTextFieldValorDoPlano.setText(String.format("%.2f", plano.getPreco()));

                jButtonSalvar.setLabel("Editar");
                jButtonExcluir.setEnabled(true);
                jTextFieldDescPlano.requestFocus();
            } else {
                jButtonSalvar.setLabel("Salvar");
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Atenção mouseclicked!"
                    + erro.getMessage());
        }
    }

