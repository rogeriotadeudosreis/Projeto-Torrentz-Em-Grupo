    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        if (jTextFieldDescPlano.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo descrição "
                    + " é obrigatório  o preenchimento!\n");
        } else if (jTextFieldQtdeAcessoSimultaneos.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo quant. de "
                    + "acesso simultâneos é obrigatório o preenchimento!\n");
        } else if (jTextFieldValorDoPlano.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo preço é"
                    + " obrigatório o preenchimento!\n");
        } else {
            try {
                int quantAcessos = Integer.parseInt(jTextFieldQtdeAcessoSimultaneos.getText());
                String descricao = jTextFieldDescPlano.getText();
                BigDecimal preco = new BigDecimal(jTextFieldValorDoPlano.getText());
                plano = new Plano(quantAcessos, descricao, preco);

                if (jButtonSalvar.getLabel().equals("Salvar")) {
                    plaBll.adicionarPlano(plano);
                } else {
                    int id = Integer.parseInt(jTextFieldCodigo.getText());
                    plano = new Plano(id, quantAcessos, descricao, preco);
                    plaBll.alterarPlano(plano);
                }
                limpaCampos();

            } catch (Exception erro) {
                JOptionPane.showMessageDialog(null, erro.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

